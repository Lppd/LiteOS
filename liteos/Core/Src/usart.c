void UART_SendByte(USART_TypeDef *uart, uint8_t data)
{     
  uart->DR = data;
  while((uart->SR & UART_FLAG_TXE) == 0);
  while((uart->SR & UART_FLAG_TC) == 0);       
}

void UART_SendData(USART_TypeDef *uart, uint8_t *buff, uint16_t size)
{
  while(size--)
  {
    uart->DR = *(buff++);
    while((uart->SR&UART_FLAG_TXE)==0);
  }

  while((uart->SR&UART_FLAG_TC)==0);       
}

